import numpy as np


def softmax(z):
  """
  计算带有防溢出技巧的 Softmax 概率分布
  支持 1D 数组（单个样本）或 2D 数组（批量样本）
  """
  # 1. 寻找最大值 (防溢出核心)
  # axis=-1 表示在最后一个维度（即类别维度）上找最大值
  # keepdims=True 保证减法时的矩阵广播 (Broadcasting) 维度匹配
  z_max = np.max(z, axis=-1, keepdims=True)

  # 2. 平移分数，将最大值拉低到 0，其余变为负数
  shifted_z = z - z_max

  # 3. 计算指数 (此时所有的指数结果都在 0 到 1 之间)
  exp_z = np.exp(shifted_z)

  # 4. 归一化计算最终概率
  probabilities = exp_z / np.sum(exp_z, axis=-1, keepdims=True)

  return probabilities


