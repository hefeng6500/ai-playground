# 测试用例：见证防溢出魔法的时刻
# ==========================================

# 假设我们有一个包含极端大数字的原始分数数组
logits = np.array([1000.0, 1001.0, 999.0])

print("--- 使用防溢出 Softmax ---")
safe_probs = softmax(logits)
print(f"计算结果: {safe_probs}")
# 预期输出: [0.24472847 0.66524096 0.09003057] (完美运行)

